//  Задайте две матрицы. Напишите программу, которая будет находить произведение двух матриц.
// Например, даны 2 матрицы:
// 2 4 | 3 4
// 3 2 | 3 3
// Результирующая матрица будет:
// 18 20
// 15 18

int m = GetSize("Enter the number of lines of the 1st matrix: ");
int n = GetSize("Enter the number of the columns of the 1st matrix and lines of the 2nd matrix: ");
int p = GetSize("Enter the number of columns of the 2nd matrix: ");


int[,] firstMartrix = new int[m, n];
GetArray(firstMartrix);
Console.WriteLine($"\nFirst Matrix:");
ShowArray(firstMartrix);

int[,] secomdMartrix = new int[n, p];
GetArray(secomdMartrix);
Console.WriteLine($"\nSecond Matrix:");
ShowArray(secomdMartrix);

int[,] resultMatrix = new int[m,p];

MultiplyMatrix(firstMartrix, secomdMartrix, resultMatrix);
Console.WriteLine($"\nProduct of 1st and 2nd matrix:");
ShowArray(resultMatrix);

void MultiplyMatrix(int[,] firstMartrix, int[,] secomdMartrix, int[,] resultMatrix)
{
  for (int i = 0; i < resultMatrix.GetLength(0); i++)
  {
    for (int j = 0; j < resultMatrix.GetLength(1); j++)
    {
      int sum = 0;
      for (int k = 0; k < firstMartrix.GetLength(1); k++)
      {
        sum += firstMartrix[i,k] * secomdMartrix[k,j];
      }
      resultMatrix[i,j] = sum;
    }
  }
}

int GetSize(string text)
{
  Console.Write(text);
return int.Parse(Console.ReadLine());
}

void GetArray(int[,] array)
{
  for (int i = 0; i < array.GetLength(0); i++)
  {
    for (int j = 0; j < array.GetLength(1); j++)
    {
      array[i, j] = new Random().Next(0,10);
    }
  }
}

void ShowArray (int[,] array)
{
  for (int i = 0; i < array.GetLength(0); i++)
  {
    for (int j = 0; j < array.GetLength(1); j++)
    {
      Console.Write(array[i,j] + " ");
    }
    Console.WriteLine();
  }
}